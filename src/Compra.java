    private void cargartabla() {
        cursor = (DefaultTableModel) this.grilla.getModel();
        int variable = 0;

        for (int c = 0; c < cursor.getRowCount(); c++) {
            if (cursor.getValueAt(c, 0).equals(txt_cod_produc.getText())) {//pregunta si el item agregado es igual al que existe en la taba
                r = Integer.parseInt(txt_cantidad.getText())//suma la cantidad nueva con la ya existente de la tabla
                        + Integer.parseInt(String.valueOf(cursor.getValueAt(c, 3)));
                r1 = r * Integer.parseInt(txt_precio.getText());//se multiplica la cantidad total * el precio unit
                cursor.setValueAt(r, c, 3);//nuevo valor de cantidad
                cursor.setValueAt(r1, c, 4);//nuevo valor de cantidad

//                cant1= Integer.parseInt(String.valueOf(cursor.getValueAt(c, 3)));
//                cant2= Integer.parseInt(txt_cantidad.getText());
//                canT=cant1+cant2;
//                txt_total.setText(formateador.format(canT));
                variable = 1;
            }
        }
        if (variable == 1) {
            txt_cod_produc.setText("");

            txt_produc.setText("");
            txt_precio.setText("");
            txt_cantidad.setText("");
            txt_cantidad.setEnabled(false);
            txt_cod_produc.requestFocus();
            txt_cod_produc.setEnabled(true);

        } else {

            int subtotal = 0;

            subtotal = (Integer.parseInt(txt_cantidad.getText())*12)
                    * Integer.parseInt(txt_precio.getText());
            total = total + subtotal;
            txt_total.setText(formateador.format(total));

            String campo[] = new String[]{txt_cod_produc.getText(), txt_produc.getText(),
                txt_precio.getText(), txt_cantidad.getText(), String.valueOf(subtotal)};
            cursor.addRow(campo);
        }
        txt_cod_produc.setText("");

        txt_produc.setText("");
        txt_precio.setText("");
        txt_cantidad.setText("");
        txt_cod_produc.requestFocus();
        txt_cod_produc.setEnabled(true);

    }

    private void limpiargrilla() {//limpiar la tabla
        while (cursor.getRowCount() > 0) {
            cursor.removeRow(0);
        }
    }
