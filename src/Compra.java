    private void llenar_combo_depo(String orden) {
        try {
            rs = con.Listar("select concat(cod_deposito,'-',descrip) "
                    + "as dep from deposito order by cod_deposito=" + orden + " desc");
            if (rs.isBeforeFirst()) {
                while (rs.next()) {
                    cbo_deposito.addItem(rs.getString("dep"));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Compra.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void generar_codigo() {
        try {
            String sql = "select coalesce(max(cod_compra),0)+1 as cod from compra";
            rs = con.Listar(sql);// Devolver en rs(variable), el resultado del primer registro
            rs.next();//devuelve todos los valores del resultado
            this.cod.setText(rs.getString("cod"));
        } catch (SQLException ex) {
            Logger.getLogger(Compra.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void fecha() {
        try {
            rs = con.Listar("select to_char(current_date,'DD/MM/YYYY')AS fecha");
            rs.next();
            this.fecha.setText(rs.getString("fecha"));
        } catch (SQLException ex) {
            Logger.getLogger(Compra.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void hora() {
        try {
            rs = con.Listar("select to_char(current_timestamp, 'HH12:MI:SS')AS hora");
            rs.next();
            this.hora.setText(rs.getString("hora"));
        } catch (SQLException ex) {
            Logger.getLogger(Compra.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


